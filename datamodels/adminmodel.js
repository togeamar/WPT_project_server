import mongoose, { Schema } from "mongoose";


const adminschema=new Schema({
    first_name:{
        type:String,
        required:true
    },
    last_name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    }
});

export default mongoose.model("admin",adminschema,"admin");